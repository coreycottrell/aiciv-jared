def ga4_report(dimensions, metrics, date_range='30daysAgo', end_date='today',
               order_by_metric=None, order_desc=True, limit=25, filters=None):
    """
    Run a GA4 Data API report.

    Args:
        dimensions (list): List of dimension names, e.g. ['pagePath', 'sessionSource']
        metrics (list): List of metric names, e.g. ['sessions', 'totalUsers']
        date_range (str): Start date, e.g. '30daysAgo', '7daysAgo', '2026-02-01'
        end_date (str): End date, e.g. 'today', '2026-03-01'
        order_by_metric (str): Metric name to order by
        order_desc (bool): Descending order (default True)
        limit (int): Max rows to return
        filters (dict): Optional dimension or metric filter

    Returns:
        dict: Raw API response with 'rows', 'dimensionHeaders', 'metricHeaders'
    """
    token = get_ga4_token()
    headers = {'Authorization': f'Bearer {token}', 'Content-Type': 'application/json'}
    url = f"https://analyticsdata.googleapis.com/v1beta/properties/{GA4_PROPERTY_ID}:runReport"

    payload = {
        "dateRanges": [{"startDate": date_range, "endDate": end_date}],
        "dimensions": [{"name": d} for d in dimensions],
        "metrics": [{"name": m} for m in metrics],
        "limit": limit
    }

    if order_by_metric:
        payload["orderBys"] = [{"metric": {"metricName": order_by_metric}, "desc": order_desc}]

    if filters:
        payload.update(filters)

    r = requests.post(url, headers=headers, json=payload)
    r.raise_for_status()
    return r.json()


