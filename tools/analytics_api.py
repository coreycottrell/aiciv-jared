def ga4_parse_rows(response):
    """
    Parse GA4 API response into list of dicts.

    Returns:
        list of dicts with dimension and metric values combined
    """
    rows_out = []
    dim_headers = [d['name'] for d in response.get('dimensionHeaders', [])]
    met_headers = [m['name'] for m in response.get('metricHeaders', [])]

    for row in response.get('rows', []):
        row_dict = {}
        for i, val in enumerate(row.get('dimensionValues', [])):
            row_dict[dim_headers[i]] = val['value']
        for i, val in enumerate(row.get('metricValues', [])):
            row_dict[met_headers[i]] = val['value']
        rows_out.append(row_dict)

    return rows_out


def get_ga4_summary(days=30):
    """
    Get a quick summary of GA4 data for the last N days.

    Returns:
        dict with sessions, users, pageviews, top pages, top channels
    """
    date_range = f'{days}daysAgo'

    # Overall metrics
    overview = ga4_report(
        dimensions=['sessionDefaultChannelGroup'],
        metrics=['sessions', 'totalUsers', 'newUsers', 'screenPageViews', 'bounceRate', 'averageSessionDuration'],
        date_range=date_range,
        order_by_metric='sessions'
    )

    # Top pages
    top_pages = ga4_report(
        dimensions=['pagePath'],
        metrics=['sessions', 'totalUsers', 'screenPageViews', 'bounceRate'],
        date_range=date_range,
        order_by_metric='sessions',
        limit=10
    )

    # Events
    events = ga4_report(
        dimensions=['eventName'],
        metrics=['eventCount', 'totalUsers'],
        date_range=date_range,
        order_by_metric='eventCount'
    )

    # Countries
    geo = ga4_report(
        dimensions=['country'],
        metrics=['sessions', 'totalUsers'],
        date_range=date_range,
        order_by_metric='sessions',
        limit=10
    )

    return {
        'period_days': days,
        'channels': ga4_parse_rows(overview),
        'top_pages': ga4_parse_rows(top_pages),
        'events': ga4_parse_rows(events),
        'countries': ga4_parse_rows(geo)
    }


