def gsc_query(dimensions, start_date=None, end_date=None, days=28, limit=25):
    """
    Query Google Search Console search analytics.

    Args:
        dimensions (list): e.g. ['query'], ['page'], ['query', 'page'], ['country']
        start_date (str): e.g. '2026-02-01' (overrides days param)
        end_date (str): e.g. '2026-03-01'
        days (int): Days of lookback if start_date not specified
        limit (int): Max rows

    Returns:
        dict: Raw API response with 'rows'
    """
    token = get_gsc_token()
    headers = {'Authorization': f'Bearer {token}', 'Content-Type': 'application/json'}
    url = f"https://www.googleapis.com/webmasters/v3/sites/{GSC_SITE_URL}/searchAnalytics/query"

    if not start_date:
        start_dt = datetime.now() - timedelta(days=days)
        start_date = start_dt.strftime('%Y-%m-%d')
    if not end_date:
        end_date = datetime.now().strftime('%Y-%m-%d')

    payload = {
        "startDate": start_date,
        "endDate": end_date,
        "dimensions": dimensions,
        "rowLimit": limit
    }

    r = requests.post(url, headers=headers, json=payload)
    r.raise_for_status()
    return r.json()


def gsc_parse_rows(response):
    """Parse GSC API response into list of dicts."""
    rows_out = []
    for row in response.get('rows', []):
        row_dict = {
            'clicks': row.get('clicks', 0),
            'impressions': row.get('impressions', 0),
            'ctr': row.get('ctr', 0),
            'position': row.get('position', 0)
        }
        keys = row.get('keys', [])
        if len(keys) == 1:
            row_dict['key'] = keys[0]
        else:
            for i, k in enumerate(keys):
                row_dict[f'key_{i}'] = k
        rows_out.append(row_dict)
    return rows_out


def gsc_list_sitemaps():
    """List all sitemaps and their indexing status."""
    token = get_gsc_token()
    headers = {'Authorization': f'Bearer {token}'}
    url = f"https://www.googleapis.com/webmasters/v3/sites/{GSC_SITE_URL}/sitemaps"
    r = requests.get(url, headers=headers)
    r.raise_for_status()
    return r.json()


def get_gsc_summary(days=28):
    """
    Get a quick Search Console summary.

    Returns:
        dict with top queries, top pages, totals
    """
    queries_raw = gsc_query(['query'], days=days, limit=20)
    pages_raw = gsc_query(['page'], days=days, limit=20)
    sitemaps = gsc_list_sitemaps()

    queries = gsc_parse_rows(queries_raw)
    pages = gsc_parse_rows(pages_raw)

    # Totals
    total_clicks = sum(q['clicks'] for q in queries)
    total_impressions = sum(q['impressions'] for q in queries)

    return {
        'period_days': days,
        'total_clicks': total_clicks,
        'total_impressions': total_impressions,
        'avg_ctr': total_clicks / total_impressions if total_impressions else 0,
        'top_queries': sorted(queries, key=lambda x: x['clicks'], reverse=True)[:10],
        'top_pages': sorted(pages, key=lambda x: x['clicks'], reverse=True)[:10],
        'sitemaps': sitemaps.get('sitemap', [])
    }


